    private void jButton_ProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ProcessActionPerformed
        try {
            if (validateFields()) {
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure?", "Warning", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {
                    EmailService emailService = new EmailService(emailConfiguration);

                    String variables = jTextArea_VariableColumnNames.getText();
                    String[] variableFields = variables.split("\\n");

                    if (textAreaHandler == null) {
                        textAreaHandler = new TextAreaHandler(textArea_log);
                    }
                    
                    emailService.setTextAreaHandler(textAreaHandler);
                    
                    emailService.readAndSendEmailFromCSV(jTextField_PathCSV.getText(),
                            jTextField_EmailColumnName.getText(),
                            jTextField_Subject.getText(),
                            jTextArea_Message.getText(), variableFields);
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(JamesUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_ProcessActionPerformed

    private boolean validateFields() throws IOException {
        boolean hasEmptyFields = jTextField_PathCSV.getText().isEmpty()
                && jTextField_PathConfig.getText().isEmpty()
                && jTextField_Subject.getText().isEmpty()
                && jTextArea_Message.getText().isEmpty();

        if (!hasEmptyFields) {
            String emailConfigJson = new String(
                    Files.readAllBytes(Paths.get(jTextField_PathConfig.getText()))
            );

            emailConfiguration = gson.fromJson(emailConfigJson, EmailConfiguration.class);

            return emailConfiguration != null;
        }

        return true;
    }

